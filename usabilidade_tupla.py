# Uso de tuplas

# Podem ser declaradas
# Exemplo:
tupla = (1, 2, 3)

# Podem ser indexadas
# Exemplo:
print(tupla[0]) # Saída: 1

# Podem ser modificadas
# Exemplo:
tupla_modificada = (1, 2, 3)
tupla_modificada = (1, 2, 4)
print(tupla_modificada) # Saída: (1, 2, 4)

# Podem ser concatenadas
# Exemplo:
tupla1 = (1, 2, 3)
tupla2 = (4, 5, 6)
tupla_concatenada = tupla1 + tupla2
print(tupla_concatenada) # Saída: (1, 2, 3, 4, 5, 6)

# Podem ser usadas em loops
# Exemplo:
for elemento in tupla:
    print(elemento)

# Podem ser usadas em condicionais
# Exemplo:
if tupla[0] == 1:
    print("Primeiro elemento é 1")

# Podem ser usadas como chaves de dicionários
# Exemplo:
dicionario = {tupla: "Valor"}
print(dicionario) # Saída: {(1, 2, 3): 'Valor'}

# Podem ser usadas como parâmetros de funções
# Exemplo:
def funcao(parametro):
    print(parametro)

funcao(tupla) # Saída: (1, 2, 3)

# Podem ser usadas como retorno de funções
# Exemplo:
def retorno():
    return tupla

valor = retorno()
print(valor) # Saída: (1, 2, 3)

